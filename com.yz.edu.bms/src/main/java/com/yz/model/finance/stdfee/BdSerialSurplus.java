package com.yz.model.finance.stdfee;

public class BdSerialSurplus {

	private String surplusId;
	private String serialMark;
	private String accType;
	private String unit;
	private String amount;
	private String status;

	public String getSurplusId() {
		return surplusId;
	}

	public void setSurplusId(String surplusId) {
		this.surplusId = surplusId;
	}

	public String getSerialMark() {
		return serialMark;
	}

	public void setSerialMark(String serialMark) {
		this.serialMark = serialMark;
	}

	public String getAccType() {
		return accType;
	}

	public void setAccType(String accType) {
		this.accType = accType;
	}

	public String getUnit() {
		return unit;
	}

	public void setUnit(String unit) {
		this.unit = unit;
	}

	public String getAmount() {
		return amount;
	}

	public void setAmount(String amount) {
		this.amount = amount;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

}
