package com.yz.model.educational;

public class BdQingshuImport {

	private String stdName;

	private String schoolRoll;

	private String qingshuId;

	private String qingshuPwd;

	public String getStdName() {
		return stdName;
	}

	public void setStdName(String stdName) {
		this.stdName = stdName;
	}

	public String getSchoolRoll() {
		return schoolRoll;
	}

	public void setSchoolRoll(String schoolRoll) {
		this.schoolRoll = schoolRoll;
	}

	public String getQingshuId() {
		return qingshuId;
	}

	public void setQingshuId(String qingshuId) {
		this.qingshuId = qingshuId;
	}

	public String getQingshuPwd() {
		return qingshuPwd;
	}

	public void setQingshuPwd(String qingshuPwd) {
		this.qingshuPwd = qingshuPwd;
	}

}
